#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	n;

	n = 0;
	while (s[n])
		n++;
	while (n >= 0)
	{
		if (s[n] == c)
			return ((char *)(&s[n]));
		n--;
	}
	return (0);
}
