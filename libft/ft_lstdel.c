#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list		*tmp;
	int			n;

	n = 0;
	if (!alst || !del)
		n = 1;
	while (*alst && n != 1)
	{
		tmp = (*alst)->next;
		del((*alst)->content, (*alst)->content_size);
		free(*alst);
		*alst = tmp;
	}
	ft_memdel((void **)alst);
}
